        internal override bool SetSelection(bool enterKeyPressed)
        {
            if (enterKeyPressed)
            { 
                if (!initialized)
                { 
                    InitializeDropDown(); 
                }
                //set the selection to our new item 
                if (selSvc != null)
                {
                    if (KeyboardHandlingService != null)
