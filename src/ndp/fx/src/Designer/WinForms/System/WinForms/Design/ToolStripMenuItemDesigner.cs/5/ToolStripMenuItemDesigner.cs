                            KeyboardHandlingService.SelectedDesignerControl = MenuItem.DropDownItems[count]; 
                            selSvc.SetSelectedComponents(null, SelectionTypes.Replace); 
                        }
                    } 
                }
                return true;
            }
            return false; 
        }
 
