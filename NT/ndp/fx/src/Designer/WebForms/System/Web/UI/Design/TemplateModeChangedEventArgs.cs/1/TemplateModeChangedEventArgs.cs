namespace System.Web.UI.Design {
    using System; 
 
    /// <include file='doc\TemplateModeChangedEventArgs.uex' path='docs/doc[@for="TemplateModeChangedEventArgs"]/*' />
    public class TemplateModeChangedEventArgs : EventArgs { 
        private TemplateGroup _newTemplateGroup;

        /// <include file='doc\TemplateModeChangedEventArgs.uex' path='docs/doc[@for="TemplateModeChangedEventArgs.TemplateModeChangedEventArgs"]/*' />
        public TemplateModeChangedEventArgs(TemplateGroup newTemplateGroup) { 
            _newTemplateGroup = newTemplateGroup;
        } 
 
        /// <include file='doc\TemplateModeChangedEventArgs.uex' path='docs/doc[@for="TemplateModeChangedEventArgs.NewTemplateGroup"]/*' />
        public TemplateGroup NewTemplateGroup { 
            get {
                return _newTemplateGroup;
            }
        } 
    }
} 

